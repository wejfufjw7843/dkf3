<?php
def swap_variables(a, b):
    """Меняет местами значения двух переменных, используя ссылки."""
    a, b = b, a  # Сначала присваиваем b значение a, а затем присваиваем a значение b


# Пример использования
a = 10
b = 20

print("Перед заменой:")
print("a =", a)
print("b =", b)

swap_variables(a, b)

print("\nПосле замены:")
print("a =", a)
print("b =", b)
?>